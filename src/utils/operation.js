export const penjumlahan = (variabelSatu, variabelDua) => {
    return variabelSatu + variabelDua;
};

export const pengurangan = (variabelSatu, variabelDua) => {
    return variabelSatu - variabelDua;
};

export const pembagian = (variabelSatu, variabelDua) => {
    return variabelSatu / variabelDua;
};

export const perkalian = (variabelSatu, variabelDua) => {
    return variabelSatu * variabelDua;
};

export const rataRata = (variabelSatu, variabelDua, variabelTiga, variabelEmpat, variabelLima) => {
    return (variabelSatu + variabelDua + variabelTiga + variabelEmpat + variabelLima) / 5;
};

export const sisaBagi = (variabelSatu, variabelDua) => {
    return variabelSatu % variabelDua;
};

export const luasPersegi = (variabelSatu, variabelDua) => {
    return variabelSatu * variabelDua;
};

export const luasLingkaran = (variabelSatu, variabelDua) => {
    return 3.14 * variabelSatu * variabelDua;
};

export const luasKubus = (variabelSatu, variabelDua, variabelTiga) => {
    return (variabelSatu * variabelDua) * 2 + (variabelSatu * variabelTiga) * 2 + (variabelDua * variabelTiga) * 2;
};

export const volKubus = (variabelSatu, variabelDua, variabelTiga) => {
    return variabelSatu * variabelDua * variabelTiga;
};